<h1 class="has-text-align-center">Organisation des Douanes dans les ports</h1>



<h5>Surveillance dans les ports&nbsp;:</h5>



<p>Les navires qui y entrent sont soumis à une surveillance plus étroite. Une embarcation de la brigade maritime des douanes stationne à la passe des grands ports. Aussitôt arrivé à quai, le navire subit la visite.</p>



<p>Dans les ports, le service se répartit de deux façons distinctes :</p>



<p><strong>1. La garde des quais</strong> est confiée à des factionnaires qui sont placés de distance en distance et dont le seul but est de s’opposer à l’embarquement ou au débarquement des marchandises sans autorisation.</p>



<p><strong>2. La garde des tentes ou magasins</strong> est assurée par des préposés chargés de surveiller les marchandises débarquées. Enfin, une brigade spéciale d’ « ambulants » exerce dans le port une surveillance spéciale analogue, quant au but, à celle de leurs camarades des frontières de terre.</p>



<p>Ici, le service est moins monotone que sur les côtes, mais, en revanche, il est très dur dans les grands ports. Au Havre, par exemple, les préposés de faction font vingt-quatre heures de service ; ils sont divisés en deux séries alternant par quart de trois heures en moyenne. Ils entrent à la caserne à 5 heures du matin, assistent à l’appel de 9 heures et s’en vont, soit d’escorte, soit à nouveau de faction jusqu’à 5 heures du soir, puis couchent enfin dans leur lit et peuvent jouir d’une bonne nuit. En effet, dans leur service de faction, ils n’ont pour se reposer que la planche du corps de garde et la pèlerine comme oreiller. </p>



<p>A qui n’est-il pas arrivé, se promenant sur la jetée d’un de nos ports, de voir hisser le pavillon jaune par tout navire avant son entrée au port ? C’est le signal convenu pour demander la visite sanitaire. Ce qui nous amène à dire quelques mots de la police sanitaire, c’est-à-dire des mesures prises pour éviter, par mer, l’introduction en France des maladies contagieuses telles que le choléra, la peste, etc. Les douaniers formant une véritable barrière mobile qu’il n’est pas possible d’éviter, et le littoral ayant été divisé en circonscriptions sanitaires, c’est encore à eux que l’on a eu recours pour l’exécution de ce service.</p>



<p>Tout navire arrivant dans un port français doit être reconnu par l’autorité sanitaire, presque toujours un officier des douanes, avant d’entrer en communication avec la terre. Ce n’est qu’une formalité. L’agent sanitaire se renseigne sur l’état de santé à bord pendant la traversée. La loi punit sévèrement (amende et prison) tout capitaine qui essayerait de s’y soustraire. Au cas où l’agent sanitaire le juge à propos, l’examen peut être poussé plus à fond et la visite médicale de l’équipage et des passagers peut être ordonnée. Le service actif des douanes coopère aussi dans une certaine mesure à la partie matérielle des opérations de la visite.</p>



<p>Il existe <strong>un corps spécial de préposés et de sous-officiers visiteurs </strong>(les emballeurs, en argot douanier) dont le rôle est d’assister le vérificateur dans la vérification des marchandises. Ce sont eux qui sondent les colis, prennent le poids des marchandises et appliquent sur les caisses vérifiées la marque M V (marchandise vérifiée).</p>



<p>C’est au service des brigades qu’incombe aussi le service d’écor (reconnaissance et dénombrement des colis) sur les deux frontières, maritime et terrestre. On entend par là le dénombrement des colis, soit à leur débarquement, soit à leur embarquement, à leur entrée ou à leur sortie des entrepôts ou magasins. Ce service, qui, à première vue, pourrait paraître négligeable, est au contraire un des plus importants. Il garantit la prise en charge des marchandises et leur décharge à leur sortie. Bien qu’il ne soit pas nécessaire d’être bachelier pour l’exécuter, il est difficile de le bien remplir. Aussi les bons « écoreurs » sont-ils fort appréciés de leurs chefs. Le préposé n’a qu’à vérifier avec certains papiers la marque et le numéro des colis qu’on lui présente, mais il faut aller vite. Sur les quais, certaines marchandises venant en un grand nombre de caisses analogues, il ne lui faut pas en laisser enlever une de plus, ce qui serait facile en raison de l’encombrement.</p>



<p>Le service des grands ports nécessitant un nombre considérable de douaniers, il n’est pas surprenant de rencontrer dans ces centres maritimes une agglomération de préposés de douanes pouvant paraître anormale.</p>



<p>Leur modeste traitement, d’une part (120 francs par mois) et la cherté de la vie et du loyer, d’autre part, faisaient qu’autrefois ceux-ci habitaient le plus souvent la banlieue de leur résidence et se trouvaient par suite éloignés de leur travail.</p>



<h5>Les casernes</h5>



<p>C’est alors que l’idée vint de les caserner, idée toute naturelle, car n’avait-on pas affaire à des soldats? Aussi de tous côtés s’élevèrent des casernes sur les deux frontières terrestre (Avricourt, Givet, Petitcroix…) et maritime (La Rochelle, Calais, Dunkerque….) dont les deux plus importantes sont sans contredit celles de Marseille et du Havre. Sans entrer dans des détails de minime importance, nous dirons cependant que la caserne possède son poste de police à l’entrée principale, son officier et son sous-officier de casernement.</p>



<p>Les agents mariés ont à leur disposition deux ou trois pièces et greniers suivant le nombre d’enfants ; quant aux célibataires, habitant des pavillons séparés de ceux des ménages, leurs dortoirs sont vastes et spacieux. Trois écoles (maternelle, de filles, de garçons) sont à la caserne.</p>



<p>A l’intérieur et contre le mur d’enceinte se trouvent d’immenses jardins symétriques divisés en portions cédées aux plus anciens douaniers mariés qui en sont en quelque sorte les propriétaires et les arrangent à leur guise. Certains, tout comme à la campagne, y ont élevé de petites cahutes en bois. Quand il est de repos, le douanier vient là fumer sa pipe, tandis qu’autour de lui peuvent gambader les bambins.</p>



<p>Un restaurant existe aussi à la caserne. Là le douanier ne paie presque jamais ses repas. Au moyen d’un numéro d’ordre, il a un compte ouvert qu’il règle au mois. Nous pouvons dire que, pour des prix minimes, il a là ce qu’il n’aurait nulle part : une nourriture saine, abondante et réconfortante.</p>



<p>D’une façon générale, il peut se nourrir pour 1,50 fr par jour. Une preuve certaine de la qualité est que beaucoup de ménagères n’hésitent pas à venir au restaurant y acheter les portions nécessaires à leur intérieur.</p>



<p>Bien qu’à la caserne le douanier ait sa pleine liberté, la discipline y est militaire ainsi que le réveil se fait au clairon le matin ; peu après a lieu l’appel par sections, lesquelles se rendent alors sous la conduite de leurs chefs aux lieux de service.</p>



<p>De même, à 7 heures du soir, après le coup de clairon, il faut que tous les enfants soient rentrés chez eux. A 10 heures sonne l’extinction des feux et la fermeture de la porte de la caserne.</p>



<h4>Les postes de porte amarres du dunkerquois</h4>



<p>&nbsp;5 stations sont présentes sur le dunkerquois : Cayenne&nbsp;&nbsp; – Risban Ouest&nbsp; –&nbsp; Leffrinckoucke – Mardyck&nbsp; – Fort-Philippe.</p>



<p>Le matériel&nbsp; est confié aux agents des douanes&nbsp; et est décrit dans la section  matériels : canon porte-amarre, fusil porte-amarres, flèche Delvigne.</p>



